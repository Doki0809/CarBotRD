async function testGhl() {
    const { data: dealer } = await supabaseAdmin.from('dealers').select('*').not('ghl_access_token', 'is', null).limit(1).single();
    const token = dealer.ghl_access_token;
    const locationId = dealer.ghl_location_id;

    // Get real contact
    let contactId = "SQFwn20OMr2ANHZCXkdI";
    try {
        const contactRes = await fetch(`https://services.leadconnectorhq.com/contacts/?locationId=${locationId}&limit=1`, {
            headers: { 'Authorization': `Bearer ${token}`, 'Version': '2021-07-28', 'Accept': 'application/json' }
        });
        const contactData = await contactRes.json();
        if (contactData.contacts && contactData.contacts.length > 0) {
            contactId = contactData.contacts[0].id;
        }
    } catch (e) { }

    // Get real userId
    let userId = "";
    try {
        const usersRes = await fetch(`https://services.leadconnectorhq.com/users/?locationId=${locationId}`, {
            headers: { 'Authorization': `Bearer ${token}`, 'Version': '2021-07-28', 'Accept': 'application/json' }
        });
        const usersData = await usersRes.json();
        if (usersData.users && usersData.users.length > 0) {
            userId = usersData.users[0].id;
        }
    } catch (e) { }

    // Get real templateId
    let templateId = dealer.ghl_template_id || "";
    try {
        console.log("Fetching templates...");
        const tplRes = await fetch(`https://services.leadconnectorhq.com/proposals/templates?locationId=${locationId}&limit=1`, {
            headers: { 'Authorization': `Bearer ${token}`, 'Version': '2021-07-28', 'Accept': 'application/json' }
        });
        const tplData = await tplRes.json();
        if (tplData.templates && tplData.templates.length > 0) {
            templateId = tplData.templates[0].id;
        }
    } catch (e) { console.error("Could not fetch template", e); }

    console.log("Using userId:", userId, "contactId:", contactId, "templateId:", templateId);

    const endpointsToTest = [
        'https://services.leadconnectorhq.com/documents-contracts/sendLink',
        'https://services.leadconnectorhq.com/documents-contracts-template/sendLink',
        'https://services.leadconnectorhq.com/documents_contracts_template/sendLink',
        'https://services.leadconnectorhq.com/documents_contracts/sendLink',
        `https://services.leadconnectorhq.com/proposals/templates/${templateId}/send`,
        `https://services.leadconnectorhq.com/documents-contracts/templates/${templateId}/send`,
        'https://services.leadconnectorhq.com/proposals/templates/send',
        'https://services.leadconnectorhq.com/documents/send'
    ];

    for (let endpoint of endpointsToTest) {
        console.log(`\nPOST ${endpoint}`);
        try {
            const res = await fetch(endpoint, {
                method: 'POST',
                headers: {
                    'Authorization': `Bearer ${token}`,
                    'Version': '2021-07-28',
                    'Content-Type': 'application/json'
                },
                body: JSON.stringify({
                    templateId: templateId || "some-id",
                    contactId: contactId,
                    locationId: locationId,
                    name: "Test generated by script",
                    userId: userId
                })
            });
            console.log(`Status: ${res.status}`);
            console.log(await res.text());
        } catch (e) {
            console.error(e.message);
        }
    }
}
